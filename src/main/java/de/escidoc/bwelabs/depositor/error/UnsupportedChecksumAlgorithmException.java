package de.escidoc.bwelabs.depositor.error;

/**
 * The UnsupportedChecksumAlgorithmException is used to indicate that a check sum algorithm is not supported by the
 * system.
 * 
 * @author ROF
 */
public class UnsupportedChecksumAlgorithmException extends WrongConfigurationContentException {

    private static final long serialVersionUID = -7727224051196308550L;

    public UnsupportedChecksumAlgorithmException(String message) {
        super(message);
    }

    public UnsupportedChecksumAlgorithmException(String message, Throwable cause) {
        super(message, cause);
    }

}